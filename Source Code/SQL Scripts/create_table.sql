CREATE TABLE healthcare_data (
    patientid FLOAT,
    appointmentid INT,
    gender VARCHAR(10),
    scheduledday DATE,
    appointmentday DATE,
    age INT,
    neighbourhood VARCHAR(100),
    scholarship BOOLEAN,
    hipertension BOOLEAN,
    diabetes BOOLEAN,
    alcoholism BOOLEAN,
    handicap BOOLEAN,
    sms_received BOOLEAN,
    showed_up BOOLEAN
);